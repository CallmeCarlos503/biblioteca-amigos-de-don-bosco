    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
        int Estado=0;
        int Rol=0;
        if(cboEstado.getSelectedIndex()==0){
            Estado=1;
        }else{
            Estado=2;
        }
        System.out.println(cboEstado.getSelectedIndex());
        
        if(cboRol.getSelectedIndex()==0){
            Rol=1;
        }else if(cboRol.getSelectedIndex()==1){
            Rol=2;
        }else if(cboRol.getSelectedIndex()==2){
            Rol=3;
        }
        UsuarioDAO DAO= new UsuarioDAO();
        String Nombre=txtNombre.getText();
        String Apellido=txtApellido.getText();
        String Carnet=txtCarnet2.getText();
        String Carnet2=txtCarnet.getText();
        int Limites=Integer.parseInt(txtLimite.getText());
        String Password= txtPassword.getText();
        DAO.Actualizar_usuario(Nombre, Apellido, Carnet, Rol, Estado, Limites, Password, Carnet2);
       jButton1.setEnabled(true);
       txtCarnet.setEditable(true);
       JOptionPane.showMessageDialog(this, "Edicion de datos","Los datos se editaron exitosamente", JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "ERROR DE DATOS","Existe un error de datos ", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

