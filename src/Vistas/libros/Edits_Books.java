    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int ID = Integer.parseInt(txtID.getText());
        ID_General=ID;
        LibrosDAO DAO = new LibrosDAO();
        Libros libros = DAO.busqueda_Libros(ID);
        String Nombre = libros.getNombre();
        if (libros.getEstado() == 1) {
            cboEstado.setSelectedIndex(0);
        } else {
            cboEstado.setSelectedIndex(1);
        }

        if (libros.getID_Categoria() == 1) {
            cboCategoria.setSelectedIndex(0);
        } else if (libros.getID() == 2) {
            cboCategoria.setSelectedIndex(1);
        } else if (libros.getID() == 3) {
            cboCategoria.setSelectedIndex(2);
        } else if (libros.getID() == 4) {
            cboCategoria.setSelectedIndex(3);
        } else if (libros.getID() == 5) {
            cboCategoria.setSelectedIndex(4);
        } else if (libros.getID() == 6) {
            cboCategoria.setSelectedIndex(5);
        }
        txtNombre.setText(libros.getNombre());
        txtCantidad.setText(libros.getCantidad_Disponible() + "");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            int Categoria=0;
        int Estado=0;
        if (cboCategoria.getSelectedIndex() == 0) {
            Categoria = 1;
        } else if (cboCategoria.getSelectedIndex() == 1) {
            Categoria = 2;
        } else if (cboCategoria.getSelectedIndex() == 2) {
            Categoria = 3;
        } else if (cboCategoria.getSelectedIndex() == 3) {
            Categoria = 4;
        } else if (cboCategoria.getSelectedIndex() == 4) {
            Categoria = 5;
        } else if (cboCategoria.getSelectedIndex() == 5) {
            Categoria =6;
        }
        if(cboEstado.getSelectedIndex()==0){
            Estado=1;
        }else{
            Estado=2;
        }
        String Nombre=txtNombre.getText();
        int Cantidad=Integer.parseInt(txtCantidad.getText());
        int ID= Integer.parseInt(txtID.getText());
        LibrosDAO DAO= new LibrosDAO();
        DAO.Actualizar_Books(Nombre, Estado, Categoria, Cantidad,ID);
        JOptionPane.showMessageDialog(this, "Actualizacion de datos","Actualizacion Exitosa", JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error de datos","El error es debido a "+e, JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

